// Function Statement:

function greet() {
    console.log("Hello!");
}
greet();

// Function Expression:

const greet = function() {
    console.log("Hello!");
};
greet();

// Anonymous Function:

const greet = function() {
    console.log("Hello!");
};
greet();

// Fat Arrow Function with Implicit Return:


const greet = () => "Hello!";
console.log(greet());