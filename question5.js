// Entry Control Loop:

// The loop condition is checked before executing the loop body.
// Example: for, while loops.

let i = 0;
while (i < 5) {
    console.log(i);
    i++;
}


// Exit Control Loop:

// The loop condition is checked after executing the loop body.
// Example: do-while loop.

let i = 0;
do {
    console.log(i);
    i++;
} while (i < 5);
